# 곱하기 혹은 더하기
# 문제 : 각 자리가 숫자(0부터9)로만 이루어진 문자열 S가 주어졌을 때, 왼쪽부터 오른쪽으로 하나씩 모든 숫자를 확인하며 숫자 사이에 'x' 혹은 '+' 연산자를 넣어 결과적으로 만들어질 수 있는 가장 큰 수를
# 구하는 프로그램을 작성하라. (단, +보다 x를 먼저 계산하는 일반적인 방식과는 달리, 모든 연산은 왼쪽에서 부터 순서대로 이루어진다고 가정한다.)

S = '02984'
result = int(S[0]) # 첫번째 숫자를 기준으로 연산 수행
for i in range(1, len(S)): # 두번째 숫자부터 진행
    num = int(S[i])
    # 두 수중에서 하나라도 0 혹은 1인 경우 곱하기보다는 더하기 수행
    if num <= 1 or result <= 1:
        result += num
    else:
        result *= num

print(result)